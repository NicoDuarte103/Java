public class alumnos {

    protected int row_nombre;
    protected int row_asistencia;

    public void setRow_asistencia(int row_asistencia) {
        this.row_asistencia = row_asistencia;
    }

    public void setRow_nombre(int row_nombre) {
        this.row_nombre = row_nombre;
    }

    public int getRow_nombre() {
        return row_nombre;
    }

    public int getRow_asistencia() {
        return row_asistencia;
    }
}