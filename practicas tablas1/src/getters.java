public class getters {
    private int a;
    private int b;
    public String c ;

    public void setA(int a){
        this.a = a;
    }
    public void setB(int b){
        this.b = b;
    }
    public void setC(String c){
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public String getC() {
        return c;
    }
}
